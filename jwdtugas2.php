<?php
// Masukkan bilangan pertama dan bilangan kedua
$bilangan1 = 10;
$bilangan2 = 7;

// Periksa apakah bilangan pertama sama dengan bilangan kedua
if ($bilangan1 == $bilangan2) {
    echo "Bilangan pertama dan bilangan kedua sama.";
} else {
    // Periksa apakah bilangan pertama lebih besar dari bilangan kedua
    if ($bilangan1 > $bilangan2) {
        echo "Bilangan pertama lebih besar dari bilangan kedua.";
    } else {
        echo "Bilangan pertama lebih kecil dari bilangan kedua.";
    }
}
?>
